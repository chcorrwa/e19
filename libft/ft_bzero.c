#include "libft.h"

void	ft_bzero(void *b, size_t len)
{
	unsigned char	*a;
	size_t			i;

	i = 0;
	a = (unsigned char *)b;
	while (i < len)
	{
		a[i] = '\0';
		i++;
	}
}
