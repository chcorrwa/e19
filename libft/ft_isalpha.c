#include "libft.h"

int		ft_isalpha(int c)
{
	int		r;
	char	a;

	a = c;
	r = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		r = r + 1;
	return (r);
}
